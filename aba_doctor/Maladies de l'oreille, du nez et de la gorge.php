                    <p>Les maladies de l'oreille, du nez et de la gorge (ORL) comprennent une variété de conditions qui affectent ces parties du corps. Elles peuvent être causées par des infections, des allergies, des troubles structurels ou des tumeurs.</p>
                    <h3>Types de maladies ORL</h3>
                    <ul>
                        <li><strong>Otite :</strong> Inflammation ou infection de l'oreille.</li>
                        <li><strong>Sinusite :</strong> Inflammation des sinus nasaux.</li>
                        <li><strong>Pharyngite :</strong> Inflammation du pharynx, causant des maux de gorge.</li>
                        <li><strong>Rhinite :</strong> Inflammation de la muqueuse nasale.</li>
                        <li><strong>Surdité :</strong> Perte partielle ou totale de l'audition.</li>
                    </ul>
                    <h3>Symptômes communs</h3>
                    <ul>
                        <li>Douleur ou inconfort dans l'oreille, le nez ou la gorge</li>
                        <li>Perte auditive</li>
                        <li>Congestion nasale</li>
                        <li>Maux de gorge</li>
                        <li>Écoulement nasal</li>
                        <li>Maux de tête</li>
                    </ul>
                    <h3>Traitements</h3>
                    
                    <p>Le traitement des maladies ORL dépend de la cause et de la gravité de la condition. Il peut inclure des médicaments, comme des antibiotiques pour les infections bactériennes, des antihistaminiques pour les allergies, ou des corticostéroïdes pour réduire l'inflammation. Dans certains cas, une intervention chirurgicale peut être nécessaire.</p>
